// CALCULANDO O RAIO DA CIRCUNFERENCIA
const pi = 3.14 // aplicando conceitos de váriavel constante ou imutavel
var raio = 10 // variável passiva de alterações
// cálculo
var area_da_circunferencia = (pi * (raio * raio))
// printando o resultado
console.log(`A área da circunferencia é ${area_da_circunferencia}!`)

